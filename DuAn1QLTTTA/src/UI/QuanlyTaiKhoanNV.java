    private void fillTable() {
        dtm = (DefaultTableModel) tbl_TKNhanVien.getModel();
        dtm.setRowCount(0);
        listND = qlTKNV.selectAll(conn);
        for (NguoiDung nd : listND) {
            Object[] row = new Object[]{
                nd.getMaNhanVien(),
                nd.getTenDangNhap(),
                nd.getMatKhau(),
                nd.getVaiTro() == 2 ? "Giảng viên" : nd.getVaiTro() == 1 ? "Kế toán" : " Quản lý"
            };
            dtm.addRow(row);
        }

    }

    private boolean nullTK() {
        Border border = BorderFactory.createLineBorder(Color.red);
        if (txt_TenDangNhap.getText().equals("") && txt_TenDangNhap.getText().isEmpty() && txt_MatKhau.getText().equals("") && txt_MatKhau.getText().isEmpty()) {
            LBL_LoiTenDangNhap.setText("Thiếu tên đăng nhập rồi nhập lại bạn nhé");
            lbl_LoiMK.setText("Thiếu mật khẩu rồi nhập lại bạn nhé");
            txt_MatKhau.setBorder(border);
            txt_TenDangNhap.setBorder(border);
            txt_TenDangNhap.requestFocus();
            return false;
        }

        if (txt_TenDangNhap.getText().equals("") && txt_TenDangNhap.getText().isEmpty()) {
            LBL_LoiTenDangNhap.setText("Thiếu tên đăng nhập rồi nhập lại bạn nhé");
            txt_TenDangNhap.setBorder(border);
            txt_TenDangNhap.requestFocus();
            return false;

        }
        if (txt_MatKhau.getText().equals("") && txt_MatKhau.getText().isEmpty()) {
            lbl_LoiMK.setText("Thiếu mật khẩu rồi nhập lại bạn nhé");
            txt_MatKhau.setBorder(border);
            txt_MatKhau.requestFocus();
            return false;
        }
        return true;
    }

    private boolean nullTimKiem() {
        Border border = BorderFactory.createLineBorder(Color.red);
        if (txt_TimKiem.getText().equals("") && txt_TimKiem.getText().isEmpty()) {
            txt_TimKiem.setText("Vui Lòng nhập đúng định dạng mã nhân viên");
            txt_TimKiem.setForeground(Color.red);
            txt_TimKiem.setBorder(border);
            txt_TimKiem.requestFocus();
            return false;
        }
        return true;
    }

    private boolean checkTrung() {
        for (NguoiDung nguoiDung : listND) {
            if (txt_TenDangNhap.getText().equals(nguoiDung.getTenDangNhap())) {
                LBL_LoiTenDangNhap.setText("Trùng tên đăng nhập rồi nhập lại bạn nhé!");
                txt_TenDangNhap.setBorder((Border) Color.red);
                txt_TenDangNhap.requestFocus();
                return false;
            }

        }
        return true;
    }

    private void clear() {
        txt_TenDangNhap.setText("");
        txt_MatKhau.setText("");
        rbn_GiangVien.isSelected();
    }

