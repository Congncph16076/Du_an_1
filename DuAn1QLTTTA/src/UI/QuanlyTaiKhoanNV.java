    private void btn_TimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TimKiemActionPerformed

        try {
            if (nullTimKiem()) {
                Border border = BorderFactory.createLineBorder(Color.black);
                txt_TimKiem.setBorder(border);
                List<NguoiDung> listND = qlTKNV.search(Integer.parseInt(txt_TimKiem.getText()), conn);
                dtm.setRowCount(0);
                for (NguoiDung nd : listND) {
                    Vector<Object> vec = new Vector<>();
                    vec.add(nd.getMaNhanVien());
                    vec.add(nd.getTenDangNhap());
                    vec.add(nd.getMatKhau());
                    vec.add(nd.getVaiTro());
                    dtm.addRow(vec);
                }
            }
        } catch (Exception e) {
            Dialog.alert(this, "Mã nhân viên không tồn tại"+e.getMessage());
            e.printStackTrace();
        }

    }//GEN-LAST:event_btn_TimKiemActionPerformed

    private void tbl_TKNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_TKNhanVienMouseClicked

        int vitri = tbl_TKNhanVien.getSelectedRow();
        if (vitri >= 0) {
            Integer row = (Integer) tbl_TKNhanVien.getValueAt(vitri, 0);
            NguoiDung nd = qlTKNV.fromTableToText(row, conn);
            if (nd != null) {
                txt_TenDangNhap.setText(nd.getTenDangNhap());
                txt_MatKhau.setText(nd.getMatKhau());
                if (nd.getVaiTro() == 2) {
                    rbn_GiangVien.setSelected(true);
                }
                if (nd.getVaiTro() == 1) {
                    rbn_KeToan.setSelected(true);
                }
            }
        }

    }//GEN-LAST:event_tbl_TKNhanVienMouseClicked

    private void btn_SuaTaiKhoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuaTaiKhoanActionPerformed

        try {
            if (nullTK()) {
                if (checkTrung()) {
                    NguoiDung nd = new NguoiDung();
                    txt_TenDangNhap.setText(nd.getTenDangNhap());
                    txt_MatKhau.setText(nd.getMatKhau());
                    int vaitro = 0;
                    if (nd.getVaiTro() == 2) {
                        rbn_GiangVien.isSelected();
                    }
                    if (nd.getVaiTro() == 1) {
                        rbn_KeToan.isSelected();
                    }
                    qlTKNV.update(nd, conn);
                    fillTable();
                    clear();
                    Dialog.alert(this, "Sửa thành công");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            Dialog.alert(this, "Sửa không thành công"+ " "+e.getMessage());
        }

    }//GEN-LAST:event_btn_SuaTaiKhoanActionPerformed

    private void btn_ThemTaiKhoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemTaiKhoanActionPerformed

        try {
            if (nullTK()) {
                if (checkTrung()) {
                    Border border = BorderFactory.createLineBorder(Color.black);
                    txt_TenDangNhap.setBorder(border);
                    txt_MatKhau.setBorder(border);
                    NguoiDung nd = new NguoiDung();
                    nd.setTenDangNhap(txt_TenDangNhap.getText());
                    nd.setMatKhau(txt_MatKhau.getText());
                    int vaitroNV = 0;
                    if (rbn_GiangVien.isSelected()) {
                        vaitroNV = 2;
                    }
                    if (rbn_KeToan.isSelected()) {
                        vaitroNV = 1;
                    }
                    nd.setVaiTro(vaitroNV);
                    boolean insertTK = qlTKNV.insert(nd, conn);
                    fillTable();
                    clear();
                    TienIchHoTro.Dialog.alert(this, "Thêm thành công");
                    return;
                }
            }
        } catch (Exception e) {
            Dialog.alert(this, "Thêm thất bại !"+ " "+e.getMessage());
            e.printStackTrace();
            return;
        }


    }//GEN-LAST:event_btn_ThemTaiKhoanActionPerformed

